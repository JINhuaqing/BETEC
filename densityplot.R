library(magrittr)
post.f <- function(r, n){
  ff <- function(x){dbeta(x, 1+r, 1+n-r)}
  ff
}

# Both method
f1 <- post.f(28, 91)
f2 <- post.f(26, 70)
f3 <- post.f(23, 46)
f4 <- post.f(31, 52)
curve(f1, 0, 1, lty=1, xlab="p", ylab="Density", xaxt="n")
curve(f2, 0, 1, lty=2, add=TRUE)
curve(f3, 0, 1, lty=3, add=TRUE)
curve(f4, 0, 1, lty=4, add=TRUE)

